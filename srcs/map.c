#include "env.h"

void		matrix_filter(t_env *env)
{
	int		i;

	i = 0;
	while (i < env->nb_room)
	{
		env->matrix[i][i] = 0;
		env->matrix[i][env->start->id] = 0;
		env->matrix[env->end->id][i] = 0;
		++i;
	}
}
